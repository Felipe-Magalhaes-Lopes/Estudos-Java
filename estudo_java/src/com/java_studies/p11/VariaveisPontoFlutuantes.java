package com.java_studies.p11;

public class VariaveisPontoFlutuantes {

	public static void main(String[] args) {

		// Por padrão o "Literal" do ponto flutuante é o Double, para indicar que é um Float, deve-se adicionar o "f" no final do valor
		double  houseValue = 4500000.00;
		float carValue = 70000.00f;
		
		System.out.println("Valor da casa a ser adquirida: " + "$" + houseValue);
		System.out.println("Valor do carro a ser adquirido: " + "$" + carValue);
		
	}

}
