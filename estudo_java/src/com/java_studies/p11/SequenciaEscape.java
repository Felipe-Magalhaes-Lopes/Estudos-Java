package com.java_studies.p11;

public class SequenciaEscape {

	public static void main(String[] args) {
		
		// "The world is yours!!!"
		System.out.println("\"The world is yours!!!\"");  // Barra invertida para formatar e fazer as aspas aparecerem
		
		// 1\4
		System.out.println("1\\4");  // Barra invertida para printar "1\4" corretamente
		
		
		
	}

}
