package com.java_studies.p11;

public class VariaveisInteiras {

	public static void main(String[] args) {
		
		byte valor01 = 44;
		
		short valor02 = 65;
		
		int valor03 = 10;
		
		long valor04 = 1000000000;
		
		System.out.println("Valor 01 = " + valor01);
		System.out.println("Valor 02 = " + valor02);
		System.out.println("Valor 03 = " + valor03);
		System.out.println("Valor 04 = " + valor04);

		
		
		
		
	}

}
