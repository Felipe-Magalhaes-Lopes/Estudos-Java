package com.java_studies.p13;

public class OperadoresAritmeticos {

	public static void main(String[] args) {
		
		int resultado = 0;
		resultado += 93;
		
		System.out.println(resultado);
		
		resultado -= 2;
		System.out.println(resultado);
		
		resultado /= 4;
		System.out.println(resultado);
		
		resultado *= 3;
		System.out.println(resultado);
		
		resultado %= 3;
		System.out.println(resultado);
		
		resultado++;
		System.out.println(resultado);
		
		
		
	}

}
