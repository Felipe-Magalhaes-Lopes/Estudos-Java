package com.loiane.javacourse.c13;

public class OperadoresAritmeticos {

	public static void main(String[] args) {
		
		int resultado = 1 + 90;
		
		
		System.out.println(resultado);
		
		resultado = resultado - 2;
		System.out.println(resultado);
		
		resultado = resultado / 4;
		System.out.println(resultado);
		
		resultado = resultado * 3;
		System.out.println(resultado);
		
		resultado = resultado % 3;
		System.out.println(resultado);
		
		resultado++;
		System.out.println(resultado);
		
		
		
	}

}
