package com.loiane.javacourse.c13;

public class OperadoresRelacionais {

	public static void main(String[] args) {

		int v1 = 2;
		int v2 = 100;
		
		System.out.println("v1 == v2: " + (v1 == v2));
		System.out.println("v1 != v2: " + (v1 != v2));
		System.out.println("v1 >= v2: " + (v1 >= v2));
		System.out.println("v1 <= v2: " + (v1 <= v2));

	}

}
